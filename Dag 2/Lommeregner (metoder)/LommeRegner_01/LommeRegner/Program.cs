
// Trin 1
double Plus(double addend1, double addend2)
{
    return addend1 + addend2;
}

// Trin 2
double Minus(double minuend, double subtrahend)
{
    return minuend - subtrahend;
}

// Trin 3
double Divider(double dividend, double divisor)
{
    return dividend / divisor;
}

// Trin 4
double Multiplikation(double multiplikand, double multiplikator)
{
    return multiplikand * multiplikator;
}

// Trin 5
double resultat = Plus(10, 5);
Console.WriteLine(resultat);

resultat = Minus(resultat, 3);
Console.WriteLine(resultat);

resultat = Divider(resultat, 4);
Console.WriteLine(resultat);

resultat = Multiplikation(resultat, 3);
Console.WriteLine(resultat);
